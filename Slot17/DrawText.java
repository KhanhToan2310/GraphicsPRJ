    private void ShowFontProperties() {
        textProperties.setText("");
        textProperties.append("Family: " + font.getFamily() + "\n");
        textProperties.append("Name: " + font.getName() + "\n");
        textProperties.append("Style: " + font.getStyle() + "\n");
        textProperties.append("Size: " + font.getSize() + "\n");
        if (g != null) {
            FontMetrics fm = g.getFontMetrics();
            textProperties.append("\nFont metrics: \n");
            textProperties.append("Ascent: " + fm.getAscent()+ "\n");
            textProperties.append("Max. Ascent: " + fm.getMaxAscent()+ "\n");
            textProperties.append("Descent: " + fm.getDescent()+ "\n");
            textProperties.append("Leading: " + fm.getLeading()+ "\n");
            textProperties.append("Height: " + fm.getHeight()+ "\n");
            textProperties.append("Max. advance: " + fm.getMaxAdvance()+ "\n");
            if (drawnText.length() > 0) {
                textProperties.append("String width: " + fm.stringWidth(drawnText) + "\n");
            }
        }
    }

    private void loadFonts() {
        GraphicsEnvironment gp = GraphicsEnvironment.getLocalGraphicsEnvironment();
        Font[] font = gp.getAllFonts();
        this.cbFont.removeAllItems();
        String fName;
        for (Font font1 : font) {
            fName = font1.getFontName();
            this.cbFont.addItem(fName);
        }

    }

