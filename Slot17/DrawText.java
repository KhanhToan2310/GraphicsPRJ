    private void pDrawMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pDrawMouseClicked
        int x = evt.getX();
        int y = evt.getY();
        g.setFont(font);
        g.setColor(color);
        drawnText = textDrawn.getText();
        g.drawString(drawnText, x, y);
        ShowFontProperties();
    }//GEN-LAST:event_pDrawMouseClicked

