    private void pnEllipsesMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnEllipsesMouseReleased
        p2 = evt.getPoint();
        int left = p1.x < p2.x? p1.x: p2.x;
        int top = p1.y < p2.y? p1.y: p2.y;
        int width = p1.x - p2.x;
        if (width < 0 ) width = -width;
        int height = p1.y - p2.y;
        if (height < 0) {
            height = -height;
        }
        if (btrDrawOnly.isSelected()) {
            g.drawOval(left, top, width, height);
        }else{
            g.fillOval(left, top, width, height);
        }
        p1 = p2 = null;
    }//GEN-LAST:event_pnEllipsesMouseReleased

