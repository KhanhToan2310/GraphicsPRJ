    private void TabbledPannelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabbledPannelMouseClicked
        int index = this.TabbledPannel.getSelectedIndex();
        switch (index) {
            case 0:
                g = this.pnLines.getGraphics();
                this.btrDrawFill.setEnabled(false);
                this.btrDrawOnly.setEnabled(true);
                break;
            case 1:
                g = this.pnEllipses.getGraphics();
                this.btrDrawFill.setEnabled(true);
                this.btrDrawOnly.setEnabled(true);
                break;
            case 2:
                g = this.pnRectangles.getGraphics();
                this.btrDrawFill.setEnabled(true);
                this.btrDrawOnly.setEnabled(true);
                break;
        }
        g.setColor(color);
    }//GEN-LAST:event_TabbledPannelMouseClicked

